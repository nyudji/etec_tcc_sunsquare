				<h2>Objetivos</h2>
				<p style="font-size:14px">
					<br>•   Manter, por meio de seus estabelecimentos penais subordinados, a custódia dos indivíduos presos provisoriamente;
					<br>•   Manter, por meio de seus estabelecimentos penais subordinados, a custódia dos indivíduos condenados ao cumprimento de penas privativas de liberdade nos regimes fechado e semiaberto;
<br>•   Manter, por meio de seus hospitais de custódia e tratamento psiquiátrico subordinados, a custódia dos indivíduos submetidos a medidas de segurança detentivas;
<br>•   Captar vagas, acompanhar e fiscalizar, por meio da Coordenadoria de Reintegração Social e Ciadadania, órgão subordinado e da Fundação “Prof. Dr. Manoel Pedro Pimentel” – FUNAP, órgão vinculado, os indivíduos submetidos ao cumprimento de penas alternativas à prisão, em especial às de prestação de serviços à comunidade;
<br>•   Acompanhar, por meio de seus estabelecimentos penais, dos hospitais de custódia e tratamento psiquiátrico subordinados, da FUNAP, órgão vinculado e da Coordenadoria de Reintegração Social e Ciadadania, órgão subordinado, o efetivo cumprimento da execução penal, possibilitando a aplicação das disposições contidas nas sentenças judiciais e nas decisões criminais;
<br>•   Proporcionar, por meio de seus estabelecimentos penais, dos hospitais de custódia e tratamento psiquiátrico subordinados e da FUNAP, órgão vinculado, as condições necessárias ao processo de reintegração social dos presos e internados;
<br>•   Proporcionar, por meio da Coordenadoria de Reintegração Social e Ciadadania, órgão subordinado e da FUNAP, órgão vinculado, atendimento aos egressos, com vistas a sua reinserção social;
<br>•   Proporcionar, por meio dos estabelecimentos penais, dos hospitais de custódia e tratamento psiquiátrico subordinados e da Coordenadoria de Reintegração Social e Ciadadania, órgão subordinado, atendimento aos familiares de presos e internados;
<br>•   Prestar, por meio dos estabelecimentos penais, das respectivas coordenadorias às quais encontram-se subordinados e da sede da secretaria, assistência material aos presos e internados;
<br>•   Prestar, por meio dos estabelecimentos penais, dos hospitais de custódia e tratamento psiquiátrico subordinados e da Coordenadoria de Saúde do Sistema Penitenciário, assistência à saúde dos presos e internados;
<br>•   Prestar, por meio dos estabelecimentos penais, dos hospitais de custódia e tratamento psiquiátrico subordinados, assistência social aos presos e internados;
<br>•   Possibilitar, por meio da FUNAP e da Defensoria Pública, a prestação de assistência jurídica aos presos e internados;
<br>•   Prestar, por meio dos estabelecimentos penais, dos hospitais de custódia e tratamento psiquiátrico subordinados e da FUNAP, órgão vinculado, assistência educacional aos presos e internados;
<br>•   Proporcionar, por meio dos estabelecimentos penais, dos hospitais de custódia e tratamento psiquiátrico subordinados e da FUNAP, condições necessárias para o desenvolvimento de atividades laborativas;
<br>•   Possibilitar, por meio das respectivas instituições religiosas, a prestação de assistência religiosa aos presos e internados;
<br>•   Propiciar, por meio dos estabelecimentos penais subordinados, dos hospitais de custódia e tratamento psiquiátrico e da FUNAP, atividades culturais e esportivas aos presos e internados;
<br>•   Executar a política estadual do sistema penitenciário;
<br>•   Emanar diretrizes e fiscalizar o cumprimento das normas de execução das penas;
<br>•  	Emanar diretrizes e fiscalizar o cumprimento das regras de disciplina e segurança impostas pelos estabelecimentos penais subordinados;
<br>•   Emanar diretrizes e fiscalizar as regras que asseguram que os presos e os internados tenham seus direitos legais preservados;
<br>•   Emanar diretrizes e fiscalizar as regras que asseguram o cumprimento dos deveres pelos presos e internados;
<br>•   Coordenar e fiscalizar as condições de funcionamento de seus órgãos subordinados;
<br>•   Adotar, por meio do Departamento de Recursos Humanos, órgão subordinado, as medidas necessárias para o preenchimento de todos os níveis de quadro de pessoal da secretaria;
<br>•   Proporcionar, por meio do Departamento de Recursos Humanos e da Escola de Administração Penitenciária, órgãos subordinados, formação, capacitação, aperfeiçoamento e atualização dos funcionários que compõem o quadro de pessoal da secretaria;
<br>•   Emanar as diretrizes necessárias para a classificação dos condenados, segundo seus antecedentes e personalidade;
<br>•   Emanar as diretrizes necessárias para a individualização das penas, mediante a classificação dos condenados;
<br>•   Viabilizar a participação da comunidade nas atividades da execução da pena, por meio de parcerias com a sociedade civil;
<br>•   Realizar estudos, pesquisas, elaboração e desenvolvimento de programas e projetos nas áreas criminológica e penitenciária, em busca da excelência dos serviços prestados.
